    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $regras = [
            'tipo'  => 'required',
            'nome'  => 'required|max:20',
            'cor'   => 'required'
        ];
        $feedbacks = [
            'required' => 'Campo obrigatório :attribute',
            'nome.max' => 'O campo nome suporta no máximo 20 caracteres'
        ];
        $request->validate($regras, $feedbacks);
        if(Categoria::create([
            'user_id'=>Auth::user()->id,
            'nome' => $request->nome,
            'tipo' => $request->tipo,
            'cor' => $request->cor
        ])){
            return redirect()->back()->with('sucesso_categoria', 'sucesso_categoria');
        }else{
            return redirect()->back()->with('erro_categoria', 'erro_categoria');
        }
    }

